use crate::ResolvedTheme;

/// Optional context passed by kara-beautify into the kara-gate
/// renderer. Carries the paths of any PNG tiles rasterized from the
/// theme's SVG slots — window_border.svg_tile today, bar tiles when
/// kara-sight picks them up. kara-gate reads these paths via its
/// existing `general { border_tile = "..." }` config key and swaps
/// the SolidColor pattern for a repeating PNG pattern in the same
/// tiny-skia render path it uses today.
///
/// When `None` is passed, the renderer behaves exactly as before.
#[derive(Debug, Default, Clone)]
pub struct KaraGateRenderContext<'a> {
    /// Absolute filesystem path of the rasterized window-border tile
    /// PNG, if the theme set `window_border.svg_tile` and the tile
    /// rasterized successfully. `None` → fall back to solid-color
    /// border fill.
    pub window_border_tile_path: Option<&'a std::path::Path>,
    /// Absolute path of the rasterized bar-module-background tile
    /// PNG. Reserved — kara-sight's module renderer will read this
    /// when the bar module cleanup lands; today the rasterizer
    /// produces the file on disk so future sessions plug straight in.
    pub bar_module_bg_tile_path: Option<&'a std::path::Path>,
}

fn hex_to_kara(hex: &str) -> String {
    format!("0x{}", hex.trim_start_matches('#'))
}

/// Render a kara-gate theme include file. kara-beautify writes this to
/// `~/.local/state/kara/generated/kara-gate-theme.conf` and the user's
/// `kara-gate.conf` pulls it in with an `include` directive near the
/// bottom (so it overrides any inline theme block above).
///
/// The palette block is always emitted. When the active theme sets a
/// `[window_border]` section, an additional `general { }` block emits
/// the width / radius overrides and the focused / unfocused colors
/// replace the semantic defaults in the `theme { }` block. Themes
/// without `[window_border]` stay palette-only, preserving the old
/// "user owns layout, beautify owns color" behavior.
pub fn render_kara_gate_theme(theme: &ResolvedTheme) -> String {
    render_kara_gate_theme_with(theme, &KaraGateRenderContext::default())
}

pub fn render_kara_gate_theme_with(
    theme: &ResolvedTheme,
    ctx: &KaraGateRenderContext<'_>,
) -> String {
    let c = &theme.semantic;

    // `theme.name` is either "<theme>" (single-palette theme) or
    // "<theme>:<variant>" (multi-variant). Split the variant off so
    // the comment shows "theme: default (variant: nord)" with a real
    // source-file path instead of the resolved identifier.
    let (theme_dir, display_header) = match theme.name.split_once(':') {
        Some((t, v)) => (t, format!("{t} (variant: {v})")),
        None => (theme.name.as_str(), theme.name.clone()),
    };

    // Resolve border colors — palette refs like "$accent" become hex,
    // literal hex passes through, missing fields fall back to the
    // semantic defaults.
    let focused_color = theme
        .window_border
        .as_ref()
        .and_then(|wb| wb.color_focused.as_deref())
        .map(|s| theme.resolve_palette_ref(s))
        .unwrap_or_else(|| c.accent.to_hex().trim_start_matches('#').to_string());
    let border_color = theme
        .window_border
        .as_ref()
        .and_then(|wb| wb.color_unfocused.as_deref())
        .map(|s| theme.resolve_palette_ref(s))
        .unwrap_or_else(|| c.border_subtle.to_hex().trim_start_matches('#').to_string());

    let mut out = format!(
        "# generated by kara-beautify for theme: {display_header}
# DO NOT EDIT — edit your theme in themes/{theme_dir}/theme.toml instead.
#
# This file is loaded via `include` from your kara-gate.conf. It
# overrides whatever inline theme / general blocks you have above
# the include line.

theme {{
    bg          {bg}
    surface     {surface}
    text        {text}
    text_muted  {text_muted}
    accent      {accent}
    accent_soft {accent_soft}
    border      {border}
}}
",
        bg = hex_to_kara(&c.bg0.to_hex()),
        surface = hex_to_kara(&c.bg1.to_hex()),
        text = hex_to_kara(&c.fg0.to_hex()),
        text_muted = hex_to_kara(&c.fg_muted.to_hex()),
        accent = hex_to_kara(&focused_color),
        accent_soft = hex_to_kara(&c.accent_soft.to_hex()),
        border = hex_to_kara(&border_color),
    );

    // Width + radius override, only when the theme opts in.
    if let Some(wb) = theme.window_border.as_ref() {
        if wb.width.is_some() || wb.radius.is_some() {
            out.push('\n');
            out.push_str("general {\n");
            if let Some(w) = wb.width {
                out.push_str(&format!("    border_px     {w}\n"));
            }
            if let Some(r) = wb.radius {
                out.push_str(&format!("    border_radius {r}\n"));
            }
            out.push_str("}\n");
        }
        if let Some(tile) = ctx.window_border_tile_path {
            // Rasterized PNG path. kara-config's General struct picks
            // up `border_tile` as an Option<PathBuf> that kara-gate's
            // border rasterizer tiles across the border rect instead
            // of the solid color above.
            out.push('\n');
            out.push_str("general {\n");
            out.push_str(&format!("    border_tile   {}\n", tile.display()));
            out.push_str("}\n");
        } else if wb.svg_tile.is_some() {
            out.push_str("\n# NOTE: [window_border.svg_tile] was set but the rasterizer\n");
            out.push_str("# did not produce a tile path (missing file / parse error).\n");
            out.push_str("# Falling back to the solid-color border above.\n");
        }
    }

    // Theme-driven bar geometry + appearance. Emits a single bar { }
    // block carrying every non-None field from theme.bar. kara-config's
    // multi-block merge means this block overrides only the specific
    // keys the theme cares about; the user's base bar { modules { } }
    // stays untouched.
    if let Some(bar) = theme.bar.as_ref() {
        let mut lines: Vec<String> = Vec::new();
        if let Some(v) = bar.background {
            lines.push(format!("    background       {v}"));
        }
        if let Some(ref s) = bar.background_color {
            lines.push(format!("    background_color 0x{}", theme.resolve_palette_ref(s)));
        }
        if let Some(v) = bar.background_alpha {
            lines.push(format!("    background_alpha {v}"));
        }
        if let Some(v) = bar.height {
            lines.push(format!("    height           {v}"));
        }
        if let Some(v) = bar.pill {
            lines.push(format!("    pill             {v}"));
        }
        if let Some(v) = bar.edge_padding_x {
            lines.push(format!("    edge_padding_x   {v}"));
        }
        if let Some(v) = bar.edge_padding_y {
            lines.push(format!("    edge_padding_y   {v}"));
        }
        if let Some(v) = bar.module_gap {
            lines.push(format!("    module_gap       {v}"));
        }
        if let Some(v) = bar.module_padding_x {
            lines.push(format!("    module_padding_x {v}"));
        }
        if let Some(v) = bar.module_padding_y {
            lines.push(format!("    module_padding_y {v}"));
        }
        if let Some(v) = bar.module_rounded {
            lines.push(format!("    module_rounded   {v}"));
        }
        if let Some(v) = bar.blur {
            lines.push(format!("    blur             {v}"));
        }
        if let Some(v) = bar.hide_center {
            lines.push(format!("    hide_center      {v}"));
        }
        if let Some(tile) = ctx.bar_module_bg_tile_path {
            lines.push(format!("    module_bg_tile   {}", tile.display()));
        }
        if let Some(ref s) = bar.module_bg {
            lines.push(format!("    module_background 0x{}", theme.resolve_palette_ref(s)));
        }
        if let Some(ref s) = bar.module_outline_color {
            lines.push(format!(
                "    module_border_color 0x{}",
                theme.resolve_palette_ref(s)
            ));
        }
        if !lines.is_empty() {
            out.push('\n');
            out.push_str("bar {\n");
            for line in &lines {
                out.push_str(line);
                out.push('\n');
            }
            out.push_str("}\n");
        }
    }

    // Theme-driven bar font override. The bar renderer reads font
    // from `general { font font_size }`, so we emit a general block
    // when the theme's [bar] section specifies font_family / font_size.
    if let Some(bar) = theme.bar.as_ref() {
        if bar.font_family.is_some() || bar.font_size.is_some() {
            out.push('\n');
            out.push_str("general {\n");
            if let Some(ref f) = bar.font_family {
                out.push_str(&format!("    font {f}\n"));
            }
            if let Some(s) = bar.font_size {
                out.push_str(&format!("    font_size {s}\n"));
            }
            out.push_str("}\n");
        }
    }

    // Cursor theme + size override. Lets mod+shift+t flip the cursor
    // live: kara-beautify rewrites this include, SIGHUPs kara-gate,
    // reload_config reparses the include, load_cursor_theme flushes
    // the named cache, and the next render frame uses the new theme.
    // Without this block, kara-gate would read only the user's
    // static cursor_theme from the base config — kara-beautify's
    // gsettings / xcursor / xsetroot writes would only reach GTK
    // apps, never the compositor.
    out.push('\n');
    out.push_str("general {\n");
    out.push_str(&format!("    cursor_theme  {}\n", theme.cursor.theme));
    out.push_str(&format!("    cursor_size   {}\n", theme.cursor.size));
    out.push_str("}\n");

    out
}
